  function generateFiveStarData(gachaDataCurrent: GachaItem[]) {
    const fiveStarItems: GachaItem[] = [];
    for (let i = 0; i < gachaDataCurrent.length; i++) {
      let item;
      item = gachaDataCurrent[i];
      item.index = i;
      if (all[i].rank_type === "5") {
        if (
          fiveStarItems.length != 0 &&
          fiveStarItems[fiveStarItems.length - 1].rank_type != "5"
        ) {
          fiveStarItems.pop();
        }

        if (fiveStarItems.length == 0) {
          item.draws = item.index;
        } else {
          item.draws =
            item.index - (fiveStarItems[fiveStarItems.length - 1].index ?? 0);
        }
      } else if (fiveStarItems.length == 0) {
        item.draws = item.index;
      } else {
        if (fiveStarItems[fiveStarItems.length - 1].rank_type != "5") {
          fiveStarItems.pop();
        }
        _calcDraws(item);
      }
      fiveStarItems.push(item);
    }
    setAllData(fiveStarItems);

    function _calcDraws(item: GachaItem) {
      if (fiveStarItems.length == 0) {
        item.draws = item.index;
      } else {
        item.draws =
          item.index! - (fiveStarItems[fiveStarItems.length - 1].index ?? 0);
      }
    }
  }

